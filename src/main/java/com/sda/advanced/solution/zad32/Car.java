package com.sda.advanced.solution.zad32;

public class Car {

	private final String model;
	private final double price;

	public Car(String model, double price) {
		this.model = model;
		this.price = price;
	}

	public String getModel() {
		return model;
	}

	public double getPrice() {
		return price;
	}
}
